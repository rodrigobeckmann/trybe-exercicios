let saldo = 0;

function adcionaSaldo(valor) {
    return saldo = saldo + valor
}

function subtraiSaldo(valor) {
    return saldo = saldo - valor
}

function multiplicaSaldo(taxa) {
    return saldo = saldo * taxa
}

function dividaSaldo(divisor) {
    return saldo = saldo / divisor
}

adcionaSaldo(1100);
subtraiSaldo(100);
multiplicaSaldo(2);
dividaSaldo(2);


console.log (saldo);